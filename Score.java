package Boat_Race;

public class Score {
	private int score;
	private String playerName;
	
	public Score(int score, String playerName) {
		this.score = score;
		this.playerName = playerName;
		
	}
	
	public int getScore() {
		return score;
	}
	
	public String getPlayerName() {
		return playerName;
	}

}
